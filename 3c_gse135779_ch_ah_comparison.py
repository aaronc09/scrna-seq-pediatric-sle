# HEATMAP

import matplotlib.pyplot as plt
import seaborn as sns

# Positive/red = stronger in aH
# Negative/blue = stronger in cH

heatmap_data = shared.copy()

heatmap_matrix = (
    heatmap_data
    .groupby(["source_ah", "target_ah"])["rank_difference"]
    .mean()
    .unstack()
    .fillna(0)
)

plt.figure(figsize=(10, 8))

sns.heatmap(
    heatmap_matrix,
    cmap="coolwarm",
    center=0,
    linewidths=0.4,
    linecolor="gray",
    cbar_kws={
        "label": "Mean rank difference\n(+ aH stronger, - cH stronger)"
    }
)

plt.title("Differential Cell–Cell Communication: aH vs cH")
plt.xlabel("Target Cell Type")
plt.ylabel("Source Cell Type")

plt.xticks(rotation=45, ha="right")
plt.yticks(rotation=0)

plt.tight_layout()
plt.show()

# NETWORK PLOT

import networkx as nx

# use top interactions only (keeps graph readable)
net_df = ah_stronger.head(30)

G = nx.DiGraph()

for _, row in net_df.iterrows():
    G.add_edge(
        row["source_ah"],
        row["target_ah"],
        weight=row["rank_difference"]
    )

plt.figure(figsize=(10, 10))

pos = nx.spring_layout(G, seed=42)

edges = G.edges(data=True)
weights = [d["weight"] for (_, _, d) in edges]

nx.draw(
    G,
    pos,
    with_labels=True,
    node_size=2000,
    font_size=10,
    width=[w * 2 for w in weights]
)

plt.title("Top aH Cell–Cell Communication Network")
plt.show()

