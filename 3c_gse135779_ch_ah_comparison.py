import os
import pandas as pd

# Input folders
ah_dir = "/content/drive/MyDrive/GSE135779/Results/aH_liana"
ch_dir = "/content/drive/MyDrive/GSE135779/Results/cH_liana"

# Output folder
out_dir = "/content/drive/MyDrive/GSE135779/Results/cH_aH_comparison"
os.makedirs(out_dir, exist_ok=True)

# Input files
ah_file = os.path.join(ah_dir, "aH_liana_res.csv")
ch_file = os.path.join(ch_dir, "cH_liana_res.csv")

# Load
res_ah = pd.read_csv(ah_file)
res_ch = pd.read_csv(ch_file)

print("aH shape:", res_ah.shape)
print("cH shape:", res_ch.shape)
display(res_ah.head())
display(res_ch.head())

def make_interaction_id(df):
    df = df.copy()
    df["interaction_id"] = (
        df["source"].astype(str) + "|" +
        df["target"].astype(str) + "|" +
        df["ligand_complex"].astype(str) + "|" +
        df["receptor_complex"].astype(str)
    )
    return df

res_ah = make_interaction_id(res_ah)
res_ch = make_interaction_id(res_ch)

res_ah["condition"] = "aH"
res_ch["condition"] = "cH"

keep_cols = [
    "interaction_id",
    "source", "target", "ligand_complex", "receptor_complex",
    "lr_means", "cellphone_pvals", "expr_prod", "scaled_weight",
    "lr_logfc", "spec_weight", "lrscore",
    "specificity_rank", "magnitude_rank",
    "condition"
]

res_ah_sub = res_ah[keep_cols].copy()
res_ch_sub = res_ch[keep_cols].copy()

# MERGED TABLE
merged = pd.merge(
    res_ch_sub.drop(columns=["condition"]),
    res_ah_sub.drop(columns=["condition"]),
    on="interaction_id",
    how="outer",
    suffixes=("_ch", "_ah")
)

# add presence labels
merged["present_in_ch"] = merged["magnitude_rank_ch"].notna()
merged["present_in_ah"] = merged["magnitude_rank_ah"].notna()

# direction of rank difference for shared interactions
merged["rank_diff_ch_minus_ah"] = (
    merged["magnitude_rank_ch"] - merged["magnitude_rank_ah"]
)

merged.to_csv(os.path.join(out_dir, "ch_vs_ah_liana_merged.csv"), index=False)

print("Saved merged file")
print(merged.shape)
display(merged.head())

# SHARED INTERACTIONS STRONGER IN aH

shared = merged[
    merged["present_in_ch"] & merged["present_in_ah"]
].copy()

shared["stronger_condition"] = np.where(
    shared["magnitude_rank_ah"] < shared["magnitude_rank_ch"],
    "ah",
    "ch"
)

shared["rank_difference"] = shared["magnitude_rank_ch"] - shared["magnitude_rank_ah"]
shared["abs_rank_difference"] = shared["rank_difference"].abs()

ah_stronger = shared[shared["stronger_condition"] == "ah"].copy()
ah_stronger = ah_stronger.sort_values(
    by=["rank_difference", "magnitude_rank_ah"],
    ascending=[False, True]
)

ah_stronger.to_csv(
    os.path.join(out_dir, "ah_stronger_shared_interactions.csv"),
    index=False
)

ah_stronger.head(50).to_csv(
    os.path.join(out_dir, "ah_stronger_top50.csv"),
    index=False
)

print("Saved aH stronger shared interactions")
display(ah_stronger.head(10))

# SHARED INTERACTIONS STRONGER IN CH

ch_stronger = shared[shared["stronger_condition"] == "ch"].copy()
ch_stronger = ch_stronger.sort_values(
    by=["rank_difference", "magnitude_rank_ch"],
    ascending=[True, True]
)

ch_stronger.to_csv(
    os.path.join(out_dir, "ch_stronger_shared_interactions.csv"),
    index=False
)

ch_stronger.head(50).to_csv(
    os.path.join(out_dir, "ch_stronger_top50.csv"),
    index=False
)

print("Saved ch stronger shared interactions")
display(ch_stronger.head(10))

# aH ONLY INTERACTIONS

ah_only = merged[
    (~merged["present_in_ch"]) & (merged["present_in_ah"])
].copy()

ah_only = ah_only.sort_values(by="magnitude_rank_ah", ascending=True)

ah_only.to_csv(
    os.path.join(out_dir, "ah_only_interactions.csv"),
    index=False
)

print("Saved aH-only interactions")
display(ah_only.head(10))

# CH ONLY INTERACTIONS

ch_only = merged[
    (merged["present_in_ch"]) & (~merged["present_in_ah"])
].copy()

ch_only = ch_only.sort_values(by="magnitude_rank_ch", ascending=True)

ch_only.to_csv(
    os.path.join(out_dir, "ch_only_interactions.csv"),
    index=False
)

print("Saved ch-only interactions")
display(ch_only.head(10))

