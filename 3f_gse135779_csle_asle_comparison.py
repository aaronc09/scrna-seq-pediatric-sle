import os
import pandas as pd
import numpy as np

from google.colab import drive
drive.mount('/content/drive')

# Input folders
asle_dir = "/content/drive/MyDrive/GSE135779/Results/aSLE_liana"
csle_dir = "/content/drive/MyDrive/GSE135779/Results/cSLE_liana"

# Output folder
out_dir = "/content/drive/MyDrive/GSE135779/Results/cSLE_aSLE_comparison"
os.makedirs(out_dir, exist_ok=True)

# Input files
asle_file = os.path.join(asle_dir, "aSLE_liana_res.csv")
csle_file = os.path.join(csle_dir, "cSLE_liana_res.csv")

# Load
res_asle = pd.read_csv(asle_file)
res_csle = pd.read_csv(csle_file)

print("aSLE shape:", res_asle.shape)
print("cSLE shape:", res_csle.shape)
display(res_asle.head())
display(res_csle.head())

def make_interaction_id(df):
    df = df.copy()
    df["interaction_id"] = (
        df["source"].astype(str) + "|" +
        df["target"].astype(str) + "|" +
        df["ligand_complex"].astype(str) + "|" +
        df["receptor_complex"].astype(str)
    )
    return df

res_asle = make_interaction_id(res_asle)
res_csle = make_interaction_id(res_csle)

res_asle["condition"] = "aSLE"
res_csle["condition"] = "cSLE"

keep_cols = [
    "interaction_id",
    "source", "target", "ligand_complex", "receptor_complex",
    "lr_means", "cellphone_pvals", "expr_prod", "scaled_weight",
    "lr_logfc", "spec_weight", "lrscore",
    "specificity_rank", "magnitude_rank",
    "condition"
]

res_asle_sub = res_asle[keep_cols].copy()
res_csle_sub = res_csle[keep_cols].copy()

# MERGED TABLE
merged = pd.merge(
    res_csle_sub.drop(columns=["condition"]),
    res_asle_sub.drop(columns=["condition"]),
    on="interaction_id",
    how="outer",
    suffixes=("_cSLE", "_aSLE")
)

merged["present_in_cSLE"] = merged["magnitude_rank_cSLE"].notna()
merged["present_in_aSLE"] = merged["magnitude_rank_aSLE"].notna()

# Positive = stronger in aSLE
# Negative = stronger in cSLE
merged["rank_diff_cSLE_minus_aSLE"] = (
    merged["magnitude_rank_cSLE"] - merged["magnitude_rank_aSLE"]
)

merged.to_csv(os.path.join(out_dir, "cSLE_vs_aSLE_liana_merged.csv"), index=False)

print("Saved merged file")
print(merged.shape)
display(merged.head())

# SHARED INTERACTIONS STRONGER IN aSLE

shared = merged[
    merged["present_in_cSLE"] & merged["present_in_aSLE"]
].copy()

shared["stronger_condition"] = np.where(
    shared["magnitude_rank_aSLE"] < shared["magnitude_rank_cSLE"],
    "aSLE",
    "cSLE"
)

shared["rank_difference"] = shared["magnitude_rank_cSLE"] - shared["magnitude_rank_aSLE"]
shared["abs_rank_difference"] = shared["rank_difference"].abs()

asle_stronger = shared[shared["stronger_condition"] == "aSLE"].copy()
asle_stronger = asle_stronger.sort_values(
    by=["rank_difference", "magnitude_rank_aSLE"],
    ascending=[False, True]
)

asle_stronger.to_csv(
    os.path.join(out_dir, "aSLE_stronger_shared_interactions.csv"),
    index=False
)

asle_stronger.head(50).to_csv(
    os.path.join(out_dir, "aSLE_stronger_top50.csv"),
    index=False
)

print("Saved aSLE stronger shared interactions")
display(asle_stronger.head(10))

# SHARED INTERACTIONS STRONGER IN cSLE

csle_stronger = shared[shared["stronger_condition"] == "cSLE"].copy()
csle_stronger = csle_stronger.sort_values(
    by=["rank_difference", "magnitude_rank_cSLE"],
    ascending=[True, True]
)

csle_stronger.to_csv(
    os.path.join(out_dir, "cSLE_stronger_shared_interactions.csv"),
    index=False
)

csle_stronger.head(50).to_csv(
    os.path.join(out_dir, "cSLE_stronger_top50.csv"),
    index=False
)

print("Saved cSLE stronger shared interactions")
display(csle_stronger.head(10))

# aSLE ONLY INTERACTIONS

asle_only = merged[
    (~merged["present_in_cSLE"]) & (merged["present_in_aSLE"])
].copy()

asle_only = asle_only.sort_values(by="magnitude_rank_aSLE", ascending=True)

asle_only.to_csv(
    os.path.join(out_dir, "aSLE_only_interactions.csv"),
    index=False
)

print("Saved aSLE-only interactions")
display(asle_only.head(10))

# cSLE ONLY INTERACTIONS

csle_only = merged[
    (merged["present_in_cSLE"]) & (~merged["present_in_aSLE"])
].copy()

csle_only = csle_only.sort_values(by="magnitude_rank_cSLE", ascending=True)

csle_only.to_csv(
    os.path.join(out_dir, "cSLE_only_interactions.csv"),
    index=False
)

print("Saved cSLE-only interactions")
display(csle_only.head(10))

# SUMMARY TABLE

summary = pd.DataFrame({
    "metric": [
        "total_interactions_cSLE",
        "total_interactions_aSLE",
        "shared_interactions",
        "cSLE_only_interactions",
        "aSLE_only_interactions",
        "shared_stronger_in_cSLE",
        "shared_stronger_in_aSLE"
    ],
    "value": [
        len(res_csle_sub),
        len(res_asle_sub),
        len(shared),
        len(csle_only),
        len(asle_only),
        len(csle_stronger),
        len(asle_stronger)
    ]
})

summary.to_csv(
    os.path.join(out_dir, "cSLE_aSLE_comparison_summary.csv"),
    index=False
)

print("Saved summary table")
display(summary)

