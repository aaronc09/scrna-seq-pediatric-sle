# HEATMAP: aSLE vs cSLE
# Positive/red = stronger in aSLE
# Negative/blue = stronger in cSLE

heatmap_data = shared.copy()

heatmap_data["rank_difference"] = (
    heatmap_data["magnitude_rank_cSLE"] -
    heatmap_data["magnitude_rank_aSLE"]
)

heatmap_matrix = (
    heatmap_data
    .groupby(["source_aSLE", "target_aSLE"])["rank_difference"]
    .mean()
    .unstack()
    .fillna(0)
)

plt.figure(figsize=(11, 8))

sns.heatmap(
    heatmap_matrix,
    cmap="coolwarm",
    center=0,
    linewidths=0.4,
    linecolor="gray",
    cbar_kws={
        "label": "Mean rank difference\n(+ aSLE stronger, - cSLE stronger)"
    }
)

plt.title("Differential Cell–Cell Communication: aSLE vs cSLE")
plt.xlabel("Target Cell Type")
plt.ylabel("Source Cell Type")

plt.xticks(rotation=45, ha="right")
plt.yticks(rotation=0)

plt.tight_layout()
plt.show()

# NETWORK PLOT

import networkx as nx

net_df = asle_stronger.head(30)

G = nx.DiGraph()

for _, row in net_df.iterrows():
    G.add_edge(
        row["source_aSLE"],
        row["target_aSLE"],
        weight=row["rank_difference"]
    )

plt.figure(figsize=(10, 10))

pos = nx.spring_layout(G, seed=42)

edges = G.edges(data=True)
weights = [d["weight"] for (_, _, d) in edges]

nx.draw(
    G,
    pos,
    with_labels=True,
    node_size=2000,
    font_size=10,
    width=[w * 2 for w in weights]
)

plt.title("Top aSLE Cell–Cell Communication Network")
plt.show()

