metric = "expr_prod"

def load_liana_file(path, cohort, metric):
    df = pd.read_csv(path)

    needed = ["source", "target", "ligand_complex", "receptor_complex", metric]
    missing = [c for c in needed if c not in df.columns]
    if missing:
        raise ValueError(f"{cohort} is missing columns: {missing}")

    out = df[needed].copy()

    out["interaction_id"] = (
        out["source"].astype(str) + "|" +
        out["target"].astype(str) + "|" +
        out["ligand_complex"].astype(str) + "|" +
        out["receptor_complex"].astype(str)
    )

    out = out.rename(columns={
        "source": f"source_{cohort}",
        "target": f"target_{cohort}",
        "ligand_complex": f"ligand_{cohort}",
        "receptor_complex": f"receptor_{cohort}",
        metric: f"{metric}_{cohort}"
    })

    return out

dfs = {
    cohort: load_liana_file(path, cohort, metric)
    for cohort, path in files.items()
}

merged = dfs["cH"]

for cohort in ["cSLE", "aH", "aSLE"]:
    merged = merged.merge(
        dfs[cohort],
        on="interaction_id",
        how="outer"
    )

for cohort in ["cH", "cSLE", "aH", "aSLE"]:
    merged[f"{metric}_{cohort}"] = merged[f"{metric}_{cohort}"].fillna(0)

def first_nonnull(row, cols):
    for c in cols:
        if pd.notna(row.get(c)):
            return row[c]
    return np.nan

merged["source"] = merged.apply(
    lambda r: first_nonnull(r, [f"source_{x}" for x in ["cSLE", "cH", "aSLE", "aH"]]),
    axis=1
)

merged["target"] = merged.apply(
    lambda r: first_nonnull(r, [f"target_{x}" for x in ["cSLE", "cH", "aSLE", "aH"]]),
    axis=1
)

merged["ligand"] = merged.apply(
    lambda r: first_nonnull(r, [f"ligand_{x}" for x in ["cSLE", "cH", "aSLE", "aH"]]),
    axis=1
)

merged["receptor"] = merged.apply(
    lambda r: first_nonnull(r, [f"receptor_{x}" for x in ["cSLE", "cH", "aSLE", "aH"]]),
    axis=1
)

merged["lr_pair"] = merged["ligand"].astype(str) + "-" + merged["receptor"].astype(str)

merged["child_disease_effect"] = merged[f"{metric}_cSLE"] - merged[f"{metric}_cH"]
merged["adult_disease_effect"] = merged[f"{metric}_aSLE"] - merged[f"{metric}_aH"]

merged["age_dependent_effect"] = (
    merged["child_disease_effect"] -
    merged["adult_disease_effect"]
)

merged["abs_age_dependent_effect"] = merged["age_dependent_effect"].abs()

merged["direction"] = np.where(
    merged["age_dependent_effect"] > 0,
    "cSLE-skewed",
    np.where(
        merged["age_dependent_effect"] < 0,
        "aSLE-skewed",
        "No difference"
    )
)

top30 = (
    merged
    .sort_values("abs_age_dependent_effect", ascending=False)
    [
        [
            "direction",
            "source",
            "target",
            "ligand",
            "receptor",
            "lr_pair",
            f"{metric}_cH",
            f"{metric}_cSLE",
            f"{metric}_aH",
            f"{metric}_aSLE",
            "child_disease_effect",
            "adult_disease_effect",
            "age_dependent_effect",
            "abs_age_dependent_effect",
            "interaction_id"
        ]
    ]
    .head(30)
)

display(top30)

