def prep_pairwise(df, healthy_label, sick_label, metric):
    source_h = f"source_{healthy_label}"
    target_h = f"target_{healthy_label}"
    ligand_h = f"ligand_complex_{healthy_label}"
    receptor_h = f"receptor_complex_{healthy_label}"
    metric_h = f"{metric}_{healthy_label}"

    source_s = f"source_{sick_label}"
    target_s = f"target_{sick_label}"
    ligand_s = f"ligand_complex_{sick_label}"
    receptor_s = f"receptor_complex_{sick_label}"
    metric_s = f"{metric}_{sick_label}"

    needed = [
        "interaction_id",
        source_h, target_h, ligand_h, receptor_h, metric_h,
        source_s, target_s, ligand_s, receptor_s, metric_s
    ]

    missing = [c for c in needed if c not in df.columns]
    if missing:
        raise ValueError(f"Missing columns: {missing}")

    out = pd.DataFrame({
        "interaction_id": df["interaction_id"],
        "source": df[source_s].fillna(df[source_h]),
        "target": df[target_s].fillna(df[target_h]),
        "ligand": df[ligand_s].fillna(df[ligand_h]),
        "receptor": df[receptor_s].fillna(df[receptor_h]),
        f"delta_{metric}_{sick_label}_minus_{healthy_label}": df[metric_s] - df[metric_h]
    })

    return out

