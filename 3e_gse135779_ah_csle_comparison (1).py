import os
import pandas as pd

# Input folders
csle_dir = "/content/drive/MyDrive/GSE135779/Results/cSLE_liana"
ah_dir   = "/content/drive/MyDrive/GSE135779/Results/aH_liana"

# Output folder
out_dir = "/content/drive/MyDrive/GSE135779/Results/aH_cSLE_comparison"
os.makedirs(out_dir, exist_ok=True)

# Input files
csle_file = os.path.join(csle_dir, "cSLE_liana_res.csv")
ah_file   = os.path.join(ah_dir, "aH_liana_res.csv")

# Load
res_csle = pd.read_csv(csle_file)
res_ah   = pd.read_csv(ah_file)

print("cSLE shape:", res_csle.shape)
print("aH shape:", res_ah.shape)
display(res_csle.head())
display(res_ah.head())

def make_interaction_id(df):
    df = df.copy()
    df["interaction_id"] = (
        df["source"].astype(str) + "|" +
        df["target"].astype(str) + "|" +
        df["ligand_complex"].astype(str) + "|" +
        df["receptor_complex"].astype(str)
    )
    return df

res_csle = make_interaction_id(res_csle)
res_ah   = make_interaction_id(res_ah)

res_csle["condition"] = "cSLE"
res_ah["condition"] = "aH"

keep_cols = [
    "interaction_id",
    "source", "target", "ligand_complex", "receptor_complex",
    "lr_means", "cellphone_pvals", "expr_prod", "scaled_weight",
    "lr_logfc", "spec_weight", "lrscore",
    "specificity_rank", "magnitude_rank",
    "condition"
]

res_csle_sub = res_csle[keep_cols].copy()
res_ah_sub   = res_ah[keep_cols].copy()

# MERGED TABLE
merged = pd.merge(
    res_ah_sub.drop(columns=["condition"]),
    res_csle_sub.drop(columns=["condition"]),
    on="interaction_id",
    how="outer",
    suffixes=("_aH", "_cSLE")
)

merged["present_in_aH"] = merged["magnitude_rank_aH"].notna()
merged["present_in_cSLE"] = merged["magnitude_rank_cSLE"].notna()

# Positive/red = stronger in cSLE
# Negative/blue = stronger in aH
merged["rank_diff_aH_minus_cSLE"] = (
    merged["magnitude_rank_aH"] - merged["magnitude_rank_cSLE"]
)

merged.to_csv(os.path.join(out_dir, "aH_vs_cSLE_liana_merged.csv"), index=False)

print("Saved merged file")
print(merged.shape)
display(merged.head())

# SHARED INTERACTIONS STRONGER IN cSLE

shared = merged[
    merged["present_in_aH"] & merged["present_in_cSLE"]
].copy()

shared["stronger_condition"] = np.where(
    shared["magnitude_rank_cSLE"] < shared["magnitude_rank_aH"],
    "cSLE",
    "aH"
)

shared["rank_difference"] = shared["magnitude_rank_aH"] - shared["magnitude_rank_cSLE"]
shared["abs_rank_difference"] = shared["rank_difference"].abs()

csle_stronger = shared[shared["stronger_condition"] == "cSLE"].copy()
csle_stronger = csle_stronger.sort_values(
    by=["rank_difference", "magnitude_rank_cSLE"],
    ascending=[False, True]
)

csle_stronger.to_csv(
    os.path.join(out_dir, "cSLE_stronger_shared_interactions.csv"),
    index=False
)

csle_stronger.head(50).to_csv(
    os.path.join(out_dir, "cSLE_stronger_top50.csv"),
    index=False
)

print("Saved cSLE stronger shared interactions")
display(csle_stronger.head(10))

# SHARED INTERACTIONS STRONGER IN aH

ah_stronger = shared[shared["stronger_condition"] == "aH"].copy()
ah_stronger = ah_stronger.sort_values(
    by=["rank_difference", "magnitude_rank_aH"],
    ascending=[True, True]
)

ah_stronger.to_csv(
    os.path.join(out_dir, "aH_stronger_shared_interactions.csv"),
    index=False
)

ah_stronger.head(50).to_csv(
    os.path.join(out_dir, "aH_stronger_top50.csv"),
    index=False
)

print("Saved aH stronger shared interactions")
display(ah_stronger.head(10))

# cSLE ONLY INTERACTIONS

csle_only = merged[
    (~merged["present_in_aH"]) & (merged["present_in_cSLE"])
].copy()

csle_only = csle_only.sort_values(by="magnitude_rank_cSLE", ascending=True)

csle_only.to_csv(
    os.path.join(out_dir, "cSLE_only_interactions.csv"),
    index=False
)

print("Saved cSLE-only interactions")
display(csle_only.head(10))

# aH ONLY INTERACTIONS

ah_only = merged[
    (merged["present_in_aH"]) & (~merged["present_in_cSLE"])
].copy()

ah_only = ah_only.sort_values(by="magnitude_rank_aH", ascending=True)

ah_only.to_csv(
    os.path.join(out_dir, "aH_only_interactions.csv"),
    index=False
)

print("Saved aH-only interactions")
display(ah_only.head(10))

# SUMMARY TABLE

summary = pd.DataFrame({
    "metric": [
        "total_interactions_aH",
        "total_interactions_cSLE",
        "shared_interactions",
        "aH_only_interactions",
        "cSLE_only_interactions",
        "shared_stronger_in_aH",
        "shared_stronger_in_cSLE"
    ],
    "value": [
        len(res_ah_sub),
        len(res_csle_sub),
        len(shared),
        len(ah_only),
        len(csle_only),
        len(ah_stronger),
        len(csle_stronger)
    ]
})

summary.to_csv(
    os.path.join(out_dir, "aH_cSLE_comparison_summary.csv"),
    index=False
)

print("Saved summary table")
display(summary)

