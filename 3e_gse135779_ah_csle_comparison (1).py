# HEATMAP: cSLE vs aH
# Positive/red = stronger in cSLE
# Negative/blue = stronger in aH

heatmap_data = shared.copy()

heatmap_data["rank_difference"] = (
    heatmap_data["magnitude_rank_aH"] -
    heatmap_data["magnitude_rank_cSLE"]
)

heatmap_matrix = (
    heatmap_data
    .groupby(["source_cSLE", "target_cSLE"])["rank_difference"]
    .mean()
    .unstack()
    .fillna(0)
)

plt.figure(figsize=(11, 8))

sns.heatmap(
    heatmap_matrix,
    cmap="coolwarm",
    center=0,
    linewidths=0.4,
    linecolor="gray",
    cbar_kws={
        "label": "Mean rank difference\n(+ cSLE stronger, - aH stronger)"
    }
)

